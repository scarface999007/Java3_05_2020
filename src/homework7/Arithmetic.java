package homework7;

public class Arithmetic {

    public int sum(int a, int b){
        return a + b;
    }

    public int diff(int a, int b){
        return a - b;
    }

    public int product(int a, int b){
        return a * b;
    }

    public int quotient(int a, int b){
        return a / b;
    }

}
